<h2 style='text-align: center; padding: 8px;'>Additional Information:</h2>
<h2 style='font-size: 24px; text-align: center; padding: 8px;'>Ectopic Pregnancy</h2>
<p>If you suspect you or your partner is pregnant because she has experienced nausea, irregular menstruation, or other common symptoms of pregnancy, but a home pregnancy test returned negative results, she may have an ectopic pregnancy. An ectopic pregnancy occurs when an ovum is fertilized outside of the uterus, most commonly within one of the fallopian tubes. It can cause lower levels of pregnancy hormones to be present in the impregnated female’s blood or urine, making it difficult for home pregnancy tests to detect ectopic pregnancies. Symptoms of ectopic pregnancy typically begin to occur six to eight weeks after the female’s last menstrual period and may include the following:</p>
<ul>
    <li>Severe, sharp, and sudden abdominal pain (in the lower abdominal area)</li>
    <li>Cramping on one side of the pelvis</li>
    <li>Nausea</li>
    <li>Lower back pain</li>
    <li>Low blood pressure</li>
    <li>Breast tenderness</li>
    <li>Bleeding or spotting</li>
    <li>Feeling dizzy or faint, or actually fainting</li>
    <li>Pain in the shoulder area (this alone may not be a sign of ectopic pregnancy but should be monitored)</li>
    <li>Full-Body Shock (occurs if the fallopian tube ruptures)</li>
</ul>
<p>To read more about the causes, treatment, and prevention of ectopic pregnancy, <a href='article/ectopic-pregnancy'>click here</a>.</p>


<h2 style='font-size: 24px; text-align: center; padding: 8px;'>The Transmission of STIs on Hormonal Birth Control</h2>
<p>When used correctly, hormonal birth control is extremely effective at preventing pregnancy. However, hormonal contraception does not prevent the spread of sexually transmitted infections (STIs). Barrier methods of contraception (e.g., the male or female condom) prevent the transmission of STIs by blocking contact between skin and bodily fluids. Here at SexInfo, we like to stress the importance of <b>both</b> partners taking responsibility for contraception and engaging in safe sex practices. We encourage “doubling-up” on contraception to protect you and your partner from unexpected pregnancies and the transmission of STIs. (It is important to remember, however, that two condoms should never be used together because the friction may lead to the condoms ripping or tearing.)</p>
<p>To learn more about the transmission of STIs, <a href="article/overview-stis">click here</a>.</p>

<h2 style='font-size: 24px; text-align: center; padding: 8px;'>Missing a Menstrual Period for Other Reasons</h2>
<p>An absent period is often the first reason many females and their partners begin to suspect a potential pregnancy. However, experiencing delayed or absent menstrual periods is more common than you may think, especially among younger females. Listed below are potential reasons (other than pregnancy) a female can miss her period</p>
<ul>
    <li>Increased stress levels</li>
    <li>Changes in diet</li>
    <li>Changes in exercise patterns</li>
    <li>Inconsistent hormonal birth control use (e.g., missing pills)</li>
    <li>Altering the hormonal birth control cycle (e.g., starting a pack at a different time of the month)</li>
    <li>Contraction or non-treatment of an STI</li>
    <li>Psychological reasons</li>
    <li>Hormonal imbalance</li>
    <li>Illness</li>
    <li>Atypical reproductive organs (e.g., only one ovary functions regularly)</li>
    <li>Sudden weight gain or loss</li>
    <li>Abortion</li>
</ul>
<p>To learn more about the menstrual cycle, <a href="article/menstrual-cycle">click here</a>.</p>
